def search_static(query_str: str, limit: int = 50) -> list:
    """Search SPELL_STATIC by name or ID. Returns list of {ID, name, source}."""
    q = query_str.strip().lower()
    results = []

    if q.isdigit():
        spell_id = int(q)
        if spell_id in SPELL_STATIC:
            name, school = SPELL_STATIC[spell_id]
            results.append({"ID": spell_id, "name": name, "source": "static"})
    else:
        for spell_id, (name, school) in SPELL_STATIC.items():
            if q in name.lower():
                results.append({"ID": spell_id, "name": name, "source": "static"})
            if len(results) >= limit:
                break

    return results


def get_static(spell_id: int) -> dict | None:
    """Get static spell data by ID."""
    if spell_id in SPELL_STATIC:
        name, school = SPELL_STATIC[spell_id]
        return {"ID": spell_id, "Name_Lang_enUS": name, "SchoolMask": school}
    return None
